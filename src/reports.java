    private void loadCheckupData() {
      DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new String[] {
        "Student Name", "Age", "Date","day", "Time Slot", "Doctor",
    });

    String query = """
        SELECT 
            ui.FullName AS student_name,
            ui.Age,
            b.date,
                   b.Day,
            b.time_slot,
            b.doctor_name
            
                   
        FROM 
            bookings b
        JOIN 
            userinfo ui ON b.user_id = ui.UserID
    """;

    try (Connection conn = connect();
         PreparedStatement pst = conn.prepareStatement(query);
         ResultSet rs = pst.executeQuery()) {

        boolean hasRows = false;

        while (rs.next()) {
            hasRows = true;
            System.out.println("Loaded: " + rs.getString("student_name"));
            model.addRow(new Object[]{
                rs.getString("student_name"),
                rs.getInt("Age"),
                rs.getDate("date"),
                rs.getString("Day"),
                rs.getString("time_slot"),
                rs.getString("doctor_name")
               
            });
        }

        if (!hasRows) {
            System.out.println("No checkup data found.");
        }

        jTable1.setModel(model);

    } catch (SQLException e) {
        e.printStackTrace();
    }
    }

